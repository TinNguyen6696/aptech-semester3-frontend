// ─── Emoji map (for category picker buttons in the form) ─────────────────────
const CATEGORY_EMOJI: Record<string, string> = {
  singer:       "🎤",
  dancer:       "💃",
  artist:       "🎨",
  designer:     "🖌️",
  coder:        "💻",
  photographer: "📷",
};

/**
 * Wraps getSingleCategoryConfig and injects the emoji field.
 * Returns: { badgeBg, badgeColor, iconBg, icon (SVG), label, key, emoji }
 */
export function getOpportunityCategoryConfig(category: string) {
  const base = getSingleCategoryConfig(category);
  const emoji = CATEGORY_EMOJI[base.key] ?? "🔖";
  return { ...base, emoji };
}
